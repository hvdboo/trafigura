from RT_INDEX_DBF I,CMC_QUOT_DBF Q, CM_MKT_DBF M, CM_FUT_DBF F, CM_FMAT1_DBF MAT
where --M_IND_LAB='SOYBEAN CBT NOV-14 USD/BU'
I.M_CATEGORY in (9,17)
and I.M_COM_QUOT=Q.M_REFERENCE and Q.M_PUBLI=M.M_REFERENCE and I.M_COM_FUT=F.M_REFERENCE and I.M_COM_MAT=MAT.M_REFERENCE
